"""
开发文档页面
介绍反推页面用到的各个数据指标的含义
"""

import streamlit as st


def page_dev_docs():
    """开发文档页面"""
    st.header("📘 说明文档")
    
    st.markdown("""
    本页面介绍反推优化功能中使用的各种数据指标及其含义，帮助用户更好地理解和使用该功能。
    """)
    
    st.subheader("1. 基础概念")
    
    st.markdown("""
    #### 基准预测值
    - **定义**：基于当前特征值，模型预测的目标变量值
    - **计算方式**：训练数据中各特征的平均值作为输入，通过模型预测得到
    - **用途**：作为优化的起点和对比基准
    
    #### 目标GMV（Gross Merchandise Volume）
    - **定义**：期望达到的业务目标值
    - **设置建议**：通常设置为基准预测值的110%-150%，避免设置过高目标导致无法实现
    
    #### 总预算
    - **定义**：所有特征值的总和上限
    - **应用场景**：在预算约束优化中使用，确保优化方案在预算范围内
    """)
    
    st.subheader("2. 优化算法")
    
    st.markdown("""
    #### 线性分配算法
    - **原理**：根据特征的SHAP值权重和敏感度，线性分配增量到各特征
    - **特点**：计算速度快，适用于简单场景
    - **公式**：Δx = (权重 × 敏感度) / (目标变化量)
    
    #### 预算约束优化算法
    - **原理**：在满足总预算约束的前提下，优化特征分配
    - **特点**：考虑预算限制，结果更贴近实际业务场景
    - **方法**：使用SLSQP优化算法求解约束优化问题
    
    #### 全约束优化算法
    - **原理**：同时考虑预算约束和各特征的上下限约束
    - **特点**：最复杂的优化算法，但结果最可靠
    - **优化目标**：最小化预测值与目标值的差异，同时考虑约束条件
    """)
    
    st.subheader("3. 特征指标")
    
    st.markdown("""
    #### SHAP权重
    - **定义**：特征对模型预测结果的重要性度量
    - **计算方式**：基于SHAP值的绝对值平均值归一化得到
    - **范围**：0-1之间，所有特征权重之和为1
    - **用途**：决定各特征在优化中的重要性
    
    #### 敏感度
    - **定义**：特征值微小变化对预测结果的影响程度
    - **计算方式**：通过数值微分方法估算
    - **用途**：影响优化过程中特征值的调整幅度
    
    #### 基准投放
    - **定义**：优化前各特征的基准值
    - **计算方式**：训练数据中各特征的平均值
    - **用途**：作为优化的起点值
    
    #### 建议投放
    - **定义**：优化算法计算出的各特征推荐值
    - **用途**：实际业务操作的参考值
    
    #### 增量
    - **定义**：建议投放值与基准投放值的差值
    - **计算方式**：建议投放 - 基准投放
    - **用途**：直观显示各特征需要调整的幅度
    """)
    
    st.subheader("4. 约束状态")
    
    st.markdown("""
    #### 正常
    - **含义**：优化结果未触及任何约束边界
    - **建议**：可直接使用优化结果
    
    #### 触达下限
    - **含义**：优化结果中某特征值低于设定的最小值
    - **建议**：检查是否需要调整最小值约束或优化目标
    
    #### 触达上限
    - **含义**：优化结果中某特征值超过设定的最大值或训练数据中的最大值
    - **建议**：检查是否需要调整最大值约束或增加预算
    """)
    
    st.subheader("5. 使用建议")
    
    st.markdown("""
    1. **合理设置目标值**：目标值不宜过高，建议设置为基准预测值的110%-150%
    2. **适当配置约束条件**：根据业务实际情况设置特征的上下限和总预算
    3. **关注约束状态**：若多数特征触达约束边界，可能需要调整约束条件
    4. **验证优化结果**：使用建议方案预测值与目标值进行对比，评估优化效果
    """)